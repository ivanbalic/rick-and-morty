class Character {
  constructor(
    id,
    name,
    type,
    image,
    gender,
    status,
    origine,
    species,
    location
  ) {
    this.id = id;
    this.name = name;
    this.type = type;
    this.image = image;
    this.gender = gender;
    this.status = status;
    this.origine = origine;
    this.species = species;
    this.location = location;
  }
}

export { Character };
